function writeProductTabs() {
  const lines = [
    "import React, { useMemo, useState } from 'react';",
    "import { Avatar, Pagination, Rate, Tabs } from 'antd';",
    "import { useTranslation } from 'react-i18next';",
    "import { ProductDetailView } from '../types/productDetailTypes';",
    "import { MOCK_REVIEWS } from '../mocks/reviews.mock';",
    "import styles from '../ProductDetailPage.module.scss';",
    '',
    'const REVIEWS_PER_PAGE = 3;',
    '',
    'interface ProductTabsProps {',
    '  product: ProductDetailView;',
    '}',
    '',
    'function ServiceTabContent({ product }: { product: ProductDetailView }) {',
    '  const { t } = useTranslation();',
    '  const afterSale = product.details.afterSale;',
    '',
    '  if (!afterSale) {',
    "    return <p className=\"text-slate-400\">{t('productDetail.defaultService')}</p>;",
    '  }',
    '',
    '  return (',
    '    <>',
    '      {afterSale.promises && afterSale.promises.length > 0 && (',
    `        <${D} className={styles.serviceGrid}>`,
    '          {afterSale.promises.map((p) => (',
    `            <${D} key={p.title} className={styles.serviceItem}>`,
    `              <${D}>`,
    `                <${D} className="text-slate-200 font-medium">{p.title}</${D}>`,
    `                <${D} className="text-slate-400 text-sm">{p.desc}</${D}>`,
    `              </${D}>`,
    `            </${D}>`,
    '          ))}',
    `        </${D}>`,
    '      )}',
    '      {afterSale.rights && afterSale.rights.length > 0 && (',
    '        <ul className="list-disc pl-5 text-slate-300 text-sm mt-4 space-y-1">',
    '          {afterSale.rights.map((r) => (',
    '            <li key={r}>{r}</li>',
    '          ))}',
    '        </ul>',
    '      )}',
    '      {afterSale.policies?.map((pol) => (',
    `        <${D} key={pol.title} className="mt-4">`,
    '          <h4 className="text-slate-200 font-medium mb-1">{pol.title}</h4>',
    '          <p className="text-slate-400 text-sm">{pol.content}</p>',
    `        </${D}>`,
    '      ))}',
    '    </>',
    '  );',
    '}',
    '',
    'const ProductTabs: React.FC<ProductTabsProps> = ({ product }) => {',
    '  const { t } = useTranslation();',
    '  const [reviewPage, setReviewPage] = useState(1);',
    '',
    '  const pagedReviews = useMemo(() => {',
    '    const start = (reviewPage - 1) * REVIEWS_PER_PAGE;',
    '    return MOCK_REVIEWS.slice(start, start + REVIEWS_PER_PAGE);',
    '  }, [reviewPage]);',
    '',
    '  const tabItems = [',
    '    {',
    "      key: 'detail',",
    "      label: t('productDetail.tabDetail'),",
    '      children: (',
    '        <>',
    `          <${D} className={styles.attrGrid}>`,
    '            {Object.entries(product.basicAttrs).map(([k, v]) => (',
    `              <${D} key={k} className={styles.attrItem}>`,
    '                <span className={styles.attrLabel}>{k}:</span>',
    '                <span className={styles.attrValue}>{v}</span>',
    `              </${D}>`,
    '            ))}',
    `          </${D}>`,
    '          {product.details.detailHtml ? (',
    `            <${D}`,
    '              className={`${styles.detailHtml} mt-6`}',
    '              dangerouslySetInnerHTML={{ __html: product.details.detailHtml }}',
    '            />',
    '          ) : (',
    "            <p className=\"text-slate-400 mt-4\">{t('productDetail.noDetail')}</p>",
    '          )}',
    '        </>',
    '      ),',
    '    },',
    '    {',
    "      key: 'specs',",
    "      label: t('productDetail.tabSpecs'),",
    '      children: (',
    '        <table className={styles.specsTable}>',
    '          <tbody>',
    '            {product.specTableRows.map((row) => (',
    '              <tr key={row.label}>',
    '                <td className={styles.specsLabel}>{row.label}</td>',
    '                <td className={styles.specsValue}>{row.value}</td>',
    '              </tr>',
    '            ))}',
    '          </tbody>',
    '        </table>',
    '      ),',
    '    },',
    '    {',
    "      key: 'reviews',",
    "      label: t('productDetail.tabReviews'),",
    '      children: (',
    '        <>',
    '          {pagedReviews.map((r) => (',
    `            <${D} key={r.id} className={styles.reviewItem}>`,
    `              <${D} className="flex items-center gap-3 mb-2">`,
    '                <Avatar>{r.userName.slice(2, 4)}</Avatar>',
    `                <${D}>`,
    `                  <${D} className="text-slate-200">{r.userName}</${D}>`,
    '                  <Rate disabled value={r.rating} className="text-xs" />',
    `                </${D}>`,
    `              </${D}>`,
    '              <p className="text-slate-400 text-xs mb-1">{r.date} · {r.specs}</p>',
    '              <p className="text-slate-300 text-sm">{r.content}</p>',
    `            </${D}>`,
    '          ))}',
    '          <Pagination',
    '            current={reviewPage}',
    '            pageSize={REVIEWS_PER_PAGE}',
    '            total={MOCK_REVIEWS.length}',
    '            onChange={setReviewPage}',
    '            className="mt-4"',
    '          />',
    '        </>',
    '      ),',
    '    },',
    '    {',
    "      key: 'service',",
    "      label: t('productDetail.tabService'),",
    '      children: <ServiceTabContent product={product} />,',
    '    },',
    '  ];',
    '',
    '  return (',
    `    <${D} className={\`\${styles.tabSection} \${styles.tabNav}\`}>`,
    '      <Tabs items={tabItems} defaultActiveKey="detail" />',
    `    </${D}>`,
    '  );',
    '};',
    '',
    'export default ProductTabs;',
    '',
  ];
  fs.writeFileSync(path.join(root, 'components/ProductTabs.tsx'), lines.join('\n'), 'utf8');
}

function writeRelatedProducts() {
  const lines = [
    "import React from 'react';",
    "import { useNavigate } from 'react-router-dom';",
    "import { Spin } from 'antd';",
    "import { useTranslation } from 'react-i18next';",
    "import ProductCard from '../../../components/ProductCard';",
    "import { Product } from '../../../services/types/productTypes';",
    "import { transformProductForDisplay } from '../../../utils/productUtils';",
    "import styles from '../ProductDetailPage.module.scss';",
    '',
    'interface RelatedProductsSectionProps {',
    '  items: Product[];',
    '  loading: boolean;',
    '}',
    '',
    'const RelatedProductsSection: React.FC<RelatedProductsSectionProps> = ({ items, loading }) => {',
    '  const { t } = useTranslation();',
    '  const navigate = useNavigate();',
    '',
    '  const handleClick = (product: Product) => {',
    '    navigate(`/marketplace/product/${product.code}`, {',
    '      state: { productId: product.id },',
    '    });',
    '  };',
    '',
    '  if (loading) {',
    `    return <${D} className="py-8 text-center"><Spin /></${D}>;`,
    '  }',
    '',
    '  if (!items.length) return null;',
    '',
    '  return (',
    `    <${D} className="mt-12">`,
    '      <h3 className="text-xl font-semibold text-white mb-4">{t(\'productDetail.relatedTitle\')}</h3>',
    `      <${D} className={styles.relatedGrid}>`,
    '        {items.map((p) => (',
    '          <ProductCard',
    '            key={p.id}',
    '            product={transformProductForDisplay(p)}',
    '            onProductClick={handleClick}',
    '          />',
    '        ))}',
    `      </${D}>`,
    `    </${D}>`,
    '  );',
    '};',
    '',
    'export default RelatedProductsSection;',
    '',
  ];
  fs.writeFileSync(path.join(root, 'components/RelatedProductsSection.tsx'), lines.join('\n'), 'utf8');
}

function writeProductDetailPage() {
  const lines = [
    "import React, { useMemo, useState } from 'react';",
    "import { useNavigate, useParams, useLocation } from 'react-router-dom';",
    "import { Breadcrumb, Button, Spin, message } from 'antd';",
    "import { useTranslation } from 'react-i18next';",
    "import { useAccount } from 'wagmi';",
    "import ProductGallery from './components/ProductGallery';",
    "import ProductBuyBox from './components/ProductBuyBox';",
    "import ProductTabs from './components/ProductTabs';",
    "import RelatedProductsSection from './components/RelatedProductsSection';",
    "import { useProductDetail, useRelatedProducts } from './hooks/useProductDetail';",
    "import { useSkuMatrix } from './hooks/useSkuMatrix';",
    "import { ProductDetailLocationState } from './types/productDetailTypes';",
    "import { cartApi } from '../../services/api/cartApi';",
    "import { favoriteApi } from '../../services/api/favoriteApi';",
    "import { redirectToAdmin } from '../../utils/redirectToAdmin';",
    "import styles from './ProductDetailPage.module.scss';",
    '',
    'const ProductDetailPage: React.FC = () => {',
    '  const { productCode } = useParams<{ productCode: string }>();',
    '  const location = useLocation();',
    '  const navigate = useNavigate();',
    '  const { t } = useTranslation();',
    '  const { isConnected } = useAccount();',
    '  const state = (location.state || {}) as ProductDetailLocationState;',
    '',
    '  const { product, loading, error, reload } = useProductDetail(',
    '    productCode,',
    '    state.productId',
    '  );',
    '',
    '  const sku = useSkuMatrix(product);',
    '  const { items: relatedItems, loading: relatedLoading } = useRelatedProducts(',
    '    product?.spu.category1Code,',
    '    product?.spu.code',
    '  );',
    '',
    '  const [isFavorited, setIsFavorited] = useState(false);',
    '  const [cartLoading, setCartLoading] = useState(false);',
    '',
    '  const galleryImages = useMemo(() => {',
    '    if (!product) return [];',
    '    const skuImgs = sku.selectedSku?.images?.length ? sku.selectedSku.images : [];',
    '    return skuImgs.length ? skuImgs : product.spu.images;',
    '  }, [product, sku.selectedSku]);',
    '',
    '  const handleAddToCart = async (quantity: number) => {',
    '    if (!product || !sku.selectedSku) return;',
    '    setCartLoading(true);',
    '    try {',
    '      const result = await cartApi.addToCart({',
    '        productId: product.spu.id,',
    '        productCode: product.spu.code,',
    '        skuId: sku.selectedSku.id,',
    '        skuCode: sku.selectedSku.skuCode,',
    '        quantity,',
    '        price: sku.selectedSku.price,',
    '        specSnapshot: sku.selectedSpecs,',
    '      });',
    '      if (result.success) {',
    "        message.success(t('productDetail.addedToCart'));",
    "        redirectToAdmin('cart');",
    '      } else {',
    "        message.error(result.message || t('productDetail.addCartFailed'));",
    '      }',
    '    } catch {',
    "      message.error(t('productDetail.addCartFailed'));",
    '    } finally {',
    '      setCartLoading(false);',
    '    }',
    '  };',
    '',
    '  const handleBuyNow = (quantity: number) => {',
    '    if (!isConnected) {',
    "      message.warning(t('productDetail.connectWalletFirst'));",
    '      return;',
    '    }',
    '    if (!product || !sku.selectedSku) return;',
    '    navigate(\'/checkout/stub\', {',
    '      state: {',
    '        productId: product.spu.id,',
    '        productCode: product.spu.code,',
    '        skuId: sku.selectedSku.id,',
    '        quantity,',
    '        price: sku.selectedSku.price,',
    '      },',
    '    });',
    '  };',
    '',
    '  const handleToggleFavorite = async () => {',
    '    if (!product) return;',
    '    const payload = { productId: product.spu.id, productCode: product.spu.code };',
    '    if (isFavorited) {',
    '      await favoriteApi.removeFavorite(payload);',
    '      setIsFavorited(false);',
    "      message.info(t('productDetail.removedFavorite'));",
    '    } else {',
    '      await favoriteApi.addFavorite(payload);',
    '      setIsFavorited(true);',
    "      message.success(t('productDetail.addedFavorite'));",
    '    }',
    '  };',
    '',
    '  if (loading) {',
    `    return <${D} className={styles.page}><Spin className="block mx-auto mt-24" /></${D}>;`,
    '  }',
    '',
    '  if (error || !product) {',
    `    return <${D} className={\`\${styles.page} text-center py-24\`}>`,
    '      <p className="text-red-400 mb-4">{error || t(\'productDetail.notFound\')}</p>',
    '      <Button onClick={() => navigate(\'/marketplace\')}>{t(\'productDetail.backToMall\')}</Button>',
    '      <Button className="ml-2" onClick={reload}>{t(\'productDetail.retry\')}</Button>',
    `    </${D}>;`,
    '  }',
    '',
    '  return (',
    `    <${D} className={styles.page}>`,
    `      <${D} className="mx-auto w-[95%] max-w-7xl pt-6">`,
    '        <Breadcrumb',
    '          className={styles.breadcrumb}',
    '          items={[',
    '            {',
    "              title: (",
    "                <span className={styles.breadcrumbLink} onClick={() => navigate('/marketplace')}>",
    "                  {t('productDetail.breadcrumbMall')}",
    '                </span>',
    '              ),',
    '            },',
    '            {',
    '              title: (',
    '                <span className={styles.breadcrumbCurrent}>{product.spu.name}</span>',
    '              ),',
    '            },',
    '          ]}',
    '        />',
    `        <${D} className={\`\${styles.glassCard} p-6 mt-4\`}>`,
    `          <${D} className={styles.mainGrid}>`,
    `            <${D}>`,
    '              <ProductGallery images={galleryImages} alt={product.spu.name} />',
    `            </${D}>`,
    `            <${D}>`,
    '              <ProductBuyBox',
    '                product={product}',
    '                specKeys={sku.specKeys}',
    '                selectedSpecs={sku.selectedSpecs}',
    '                selectedSku={sku.selectedSku}',
    '                canPurchase={sku.canPurchase}',
    '                onSelectSpec={sku.onSelectSpec}',
    '                onAddToCart={handleAddToCart}',
    '                onBuyNow={handleBuyNow}',
    '                isFavorited={isFavorited}',
    '                onToggleFavorite={handleToggleFavorite}',
    '                cartLoading={cartLoading}',
    '              />',
    `            </${D}>`,
    `          </${D}>`,
    '          <ProductTabs product={product} />',
    `        </${D}>`,
    '        <RelatedProductsSection items={relatedItems} loading={relatedLoading} />',
    `      </${D}>`,
    `    </${D}>`,
    '  );',
    '};',
    '',
    'export default ProductDetailPage;',
    '',
  ];
  fs.writeFileSync(path.join(root, 'ProductDetailPage.tsx'), lines.join('\n'), 'utf8');
}

